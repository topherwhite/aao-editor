					"Î",		"ï",		"Ï",		"ñ",		"Ñ",		"ó",
					"Ó",		"ò",		"Ò",		"ô",		"Ô",		"ø",
					"Ø",		"õ",		"Õ",		"ö",		"Ö",		"ß",
					"ú",		"Ú",		"ù",		"Ù",		"û",		"Û",
					"ü",		"Ü",		"ÿ",		"“",		"”",		"–",
					"’",		"‘",		"€",		"…"
	);
	$repl = array(	"&#192;",	"&#224;",	"&#193;",	"&#225;",	"&#194;",	"&#226;",
					"&#195;",	"&#227;",	"&#196;",	"&#228;",	"&#197;",	"&#229;",
					"&#256;",	"&#257;",	"&#258;",	"&#259;",	"&#260;",	"&#261;",
