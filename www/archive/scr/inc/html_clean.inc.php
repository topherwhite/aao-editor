	);
	
	return str_replace($orig,$repl,$instr);
}

function xml_clean_simple($in)
{
	$orig = array(	"<",		">",		"\"",		"&"		);
	$repl = array(	"&lt;",		"&gt;",		"&quot;",	"&amp;"	);
	
	return str_replace($orig,$repl,$in);
}

