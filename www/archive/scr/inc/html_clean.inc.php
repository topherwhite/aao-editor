					"&#508;",	"&#509;",	"¶",		"&#231;",	"&#199;",	"&#233;",
					"&#201;",	"&#232;",	"&#200;",	"&#234;",	"&#202;",	"&#235;",
					"&#203;",	"&#237;",	"&#205;",	"&#236;",	"&#204;",	"&#238;",
